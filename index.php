class Driver
{
  private $name;
  private $position;

  public function __construct($name, $position)
  {
    $this->name = $name;
    $this->position = $position;
  }

  public function getName()
  {
    return $this->name;
  }

  public function getPosition()
  {
    return $this->position;
  }

  public function setPosition($position)
  {
    $this->position = $position;
  }
}

class Action
{
  private $name;
  private $change;

  public function __construct($name, $change)
  {
    $this->name = $name;
    $this->change = $change;
  }

  public function getName()
  {
    return $this->name;
  }

  public function getChange()
  {
    return $this->change;
  }
}

